print("""Question-
		7. How do you type a tuple value that only contains the integer 42?""")


def main():

	print("""Answer-
		(42,) (After integer value comma is mandatory. otherwise its considered as a int by python Interpreter).\n""")
	tup1 = (42)			# int
	tup2 = (42,)		# Tuple
	print("\t   ", type(tup1))
	print("\t   ", type(tup2))

	print("\n-----------------------------------------_Thank You_-----------------------------------------------")


if __name__ == "__main__":
	main()
